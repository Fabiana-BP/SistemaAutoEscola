    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:

        switch (this.auxCategoria) {
            case "A":
                this.title = "Veículos categoria A";
                break;
            case "B":
                this.title = "Veículos categoria B";
            case "C":
                this.title = "Veículos categoria C";
                break;
            case "D":
                this.title = "Veículos categoria D";
                break;
            case "E":
                this.title = "Veículos categoria E";
                break;
            default:
                JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado!");
                break;
        }

        Conexao con = new Conexao();
        String dadosVeiculo = "SELECT * "
                + " FROM veiculos V"
                + " WHERE  V.id_aut=" + PermissaoAcesso.getIdAutoEscola() + " AND " + "V.tipo =" + "'" + this.auxCategoria + "'";
        PreparedStatement ps = null;
        ResultSet rs = null;
        //System.out.println("id_aut="+PermissaoAcesso.getIdAutoEscola());
        try {
            ps = con.getConexao().prepareStatement(dadosVeiculo);
            rs = ps.executeQuery();
            jTableCursos.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao manipular dados, verifique os dados");
            //Logger.getLogger(TelaDadosCFC.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formComponentShown

